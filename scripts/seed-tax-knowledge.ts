function ensureDir(dir: string): void {
  if (!fs.existsSync(dir)) {
    fs.mkdirSync(dir, { recursive: true });
    console.log(`✓ created dir: ${dir}`);
  }
}

function writeFile(filePath: string, content: string): void {
  if (fs.existsSync(filePath)) {
    console.log(`  skip (exists): ${filePath}`);
    return;
  }
  fs.writeFileSync(filePath, content, "utf-8");
  console.log(`✓ wrote: ${filePath}`);
}

async function main(): Promise<void> {
  console.log(`\nSeeding tax knowledge files to: ${BASE_DIR}\n`);
  ensureDir(BASE_DIR);

  for (const file of FILES) {
    const dir = path.join(BASE_DIR, file.folder);
    ensureDir(dir);
    const filePath = path.join(dir, file.filename);
    writeFile(filePath, file.content);
  }

  console.log(`\n✅ Done — ${FILES.length} files processed.`);
  console.log(`\nTo update an existing file, delete it first then re-run.\n`);
}

main().catch((err) => {
  console.error("Error:", err);
  process.exit(1);
});
